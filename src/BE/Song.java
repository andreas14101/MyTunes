package BE;

public class Song {

    private String title;
    private String artist;
    private int length;
    private String category;

    public Song() {
    }

    public String getTitle() {
        return title;
    }

    public String getArtist() {
        return artist;
    }

    public int getLength() {
        return length;
    }

    public String getCategory() {
        return category;
    }
}
